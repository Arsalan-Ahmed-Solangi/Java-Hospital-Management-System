    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
  
          
        
        String email_text = email.getText();
        String password_text = password.getText();
       
        if(!email_text.isEmpty() && !password_text.isEmpty())
        {
            
            //***Start of Login*********//
            String query = "SELECT * FROM admin WHERE `email`='"+email_text+"' AND pass='"+password_text+"'";
            ResultSet resultSet = new Database().selectWhere(query);
            try {
                if(resultSet.next())
                {
                    setVisible(false);
                    new Home().setVisible(true);
                }else{
                     JOptionPane.showMessageDialog(null,"Invalid credentials!");
                }
                //***End of Login********//
            } catch (SQLException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }else
        {
          JOptionPane.showMessageDialog(null,"Email and Password is required!");
        }
        
        
    }//GEN-LAST:event_loginBtnActionPerformed

